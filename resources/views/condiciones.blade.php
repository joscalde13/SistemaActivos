    <div class="p-6 bg-white rounded-lg shadow">
        <h1 class="text-2xl font-bold text-gray-800 mb-4">Política de Gestión de Activos de TI</h1>

        <div class="space-y-4 text-gray-700">
            <h2 class="font-semibold text-xl">Carlinux Incorporation S.A.</h2>

            <h3 class="font-semibold text-lg">1. Objetivo</h3>
            <p>Establecer las normas y lineamientos que Carlinux Incorporation S.A. debe seguir para la adquisición, registro, asignación, uso, reasignación, baja y control de sus activos de Tecnologías de la Información (TI), garantizando su correcta administración, resguardo y trazabilidad durante todo su ciclo de vida.</p>

            <h3 class="font-semibold text-lg">2. Alcance</h3>
            <p>Esta política aplica a todos los activos de TI propiedad de Carlinux Incorporation S.A., incluyendo, pero no limitándose a: computadoras de escritorio, portátiles, servidores, impresoras, dispositivos móviles, periféricos, equipos de red y cualquier otro recurso tecnológico. Es de aplicación obligatoria para todo el personal de la empresa, incluyendo empleados, contratistas y proveedores que tengan bajo su responsabilidad un activo de TI.</p>

            <h3 class="font-semibold text-lg">3. Definiciones</h3>
            <ul class="list-disc list-inside">
                <li><strong>Activo de TI:</strong> Bien tangible o intangible relacionado con tecnologías de la información que tiene valor para Carlinux Incorporation S.A.</li>
                <li><strong>Inventario de activos:</strong> Registro oficial y actualizado de todos los activos de TI, con información sobre su estado, ubicación, usuario asignado y características técnicas.</li>
                <li><strong>Ciclo de vida del activo:</strong> Etapas que atraviesa un activo desde su adquisición hasta su baja definitiva (alta, asignación, reasignación, robo, baja).</li>
                <li><strong>Área de TI:</strong> Unidad responsable de la gestión y control de los activos tecnológicos de la empresa.</li>
            </ul>

            <h3 class="font-semibold text-lg">4. Lineamientos Generales</h3>
            <ol class="list-decimal list-inside space-y-2">
                <li>
                    <strong>Registro obligatorio:</strong>
                    <ul class="list-disc list-inside ml-4">
                        <li>Todo activo de TI adquirido por Carlinux Incorporation S.A. debe ser registrado en el inventario antes de su uso.</li>
                        <li>Ningún equipo podrá ser asignado sin contar con etiqueta o código de identificación único.</li>
                    </ul>
                </li>
                <li>
                    <strong>Asignación controlada:</strong>
                    <ul class="list-disc list-inside ml-4">
                        <li>La asignación de activos se realizará únicamente si hay disponibilidad confirmada en inventario.</li>
                        <li>Todo usuario deberá firmar un acta de entrega y aceptar las condiciones de uso del equipo.</li>
                    </ul>
                </li>
                <li>
                    <strong>Uso y resguardo:</strong>
                    <ul class="list-disc list-inside ml-4">
                        <li>Los activos asignados son responsabilidad directa del usuario hasta su devolución.</li>
                        <li>El equipo debe utilizarse exclusivamente para fines laborales autorizados por Carlinux Incorporation S.A.</li>
                    </ul>
                </li>
                <li>
                    <strong>Reasignación y devoluciones:</strong>
                    <ul class="list-disc list-inside ml-4">
                        <li>Todo activo devuelto al área de TI será revisado, mantenido y limpiado antes de ser reasignado.</li>
                        <li>El inventario debe actualizarse inmediatamente con el nuevo usuario asignado.</li>
                    </ul>
                </li>
                <li>
                    <strong>Robo o pérdida:</strong>
                    <ul class="list-disc list-inside ml-4">
                        <li>El usuario debe reportar el incidente a su jefe y al área de TI de forma inmediata.</li>
                        <li>Debe presentarse denuncia ante las autoridades competentes en un plazo máximo de 24 horas.</li>
                        <li>TI registrará el activo como "Robado" y gestionará, si corresponde, la reposición.</li>
                    </ul>
                </li>
                <li>
                    <strong>Baja de activos:</strong>
                    <ul class="list-disc list-inside ml-4">
                        <li>Solo se dará de baja un activo previa evaluación técnica y autorización de la gerencia.</li>
                        <li>Todo activo en baja será borrado, destruido o reciclado según normativa ambiental y de seguridad de datos.</li>
                    </ul>
                </li>
                <li>
                    <strong>Actualización del inventario:</strong>
                    <ul class="list-disc list-inside ml-4">
                        <li>El inventario de activos deberá mantenerse actualizado en todo momento, reflejando estado, ubicación y usuario responsable.</li>
                    </ul>
                </li>
            </ol>

            <h3 class="font-semibold text-lg">5. Responsabilidades</h3>
            <ul class="list-disc list-inside">
                <li>
                    <strong>Área de TI:</strong>
                    <ul class="list-disc list-inside ml-4">
                        <li>Gestionar el inventario de activos.</li>
                        <li>Garantizar el etiquetado, registro y control.</li>
                        <li>Coordinar asignaciones, reasignaciones y bajas.</li>
                        <li>Realizar mantenimiento preventivo y correctivo.</li>
                    </ul>
                </li>
                <li>
                    <strong>Usuarios y áreas solicitantes:</strong>
                    <ul class="list-disc list-inside ml-4">
                        <li>Solicitar activos siguiendo el procedimiento establecido.</li>
                        <li>Firmar acta de entrega y aceptar condiciones de uso.</li>
                        <li>Reportar incidencias, robos o pérdidas de forma inmediata.</li>
                    </ul>
                </li>
                <li>
                    <strong>Gerencia/Dirección:</strong>
                    <ul class="list-disc list-inside ml-4">
                        <li>Autorizar bajas de activos.</li>
                        <li>Proveer recursos para la adquisición y reposición de equipos.</li>
                    </ul>
                </li>
            </ul>

            <h3 class="font-semibold text-lg">6. Cumplimiento</h3>
            <p>El incumplimiento de esta política podrá derivar en acciones correctivas y sanciones administrativas, de acuerdo con el reglamento interno de Carlinux Incorporation S.A. y la legislación vigente.</p>

            <h3 class="font-semibold text-lg">7. Vigencia</h3>
            <p>Esta política entra en vigor a partir de su aprobación y permanecerá activa hasta que sea reemplazada o actualizada por una nueva versión.</p>
